public class EVA1_8_OPERACIONES2 {

    public static void main(String[] args) {
        System.out.println("Hello World!");
        String nombre = "Armando";
        String apellido = "Rodriguez";
        String nombreCom;
        //CADENA DE TEXTO LA OPERACION SE LLENA
        //CONCATENACION -->UNIR CADENA DE TEXTO 
        //El operador de concatenacion es el +
        //cuando un simbolo tiene varias funciones
        //se le llama sobrecarga (de operaciones)
        nombreCom = nombre + "  " + apellido;
        
        System.out.println("El nombre es:  " + nombre );
        System.out.println("El apellido es:  " + apellido );
        System.out.println("El nombre es:  " + nombreCom );
    
    
    
    
    
    
    }
}
